class ArrayEx1 {
    public static void main(String[] args) {
        // Declare and initialize an array of integers
        int nums[] = new int[4];

        // Initialize the array elements
        nums[0] = 10;
        nums[1] = 20;
        nums[2] = 30;
        nums[3] = 40;

        // Access and print each element of the array
        for (int i = 0; i < 4;i++) {
            System.out.println(nums[i]);
        }
    }
}